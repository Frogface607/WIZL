// Region filter groups
export type RegionFilter = "all" | "nearby" | "thailand" | "netherlands" | "usa" | "canada";

export function filterByRegion(allShops: Shop[], region: RegionFilter): Shop[] {
  switch (region) {
    case "thailand":
      return allShops.filter((s) => s.country === "Thailand");
    case "netherlands":
      return allShops.filter((s) => s.country === "Netherlands");
    case "usa":
      return allShops.filter((s) => s.country === "USA");
    case "canada":
      return allShops.filter((s) => s.country === "Canada");
    case "nearby":
    case "all":
    default:
      return allShops;
  }
}
